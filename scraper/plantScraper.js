log.setLevel(log.LEVELS.WARNING);


// Apify.main() function wraps the crawler logic (it is optional).
Apify.main(async () => {
    // Create an instance of the RequestList class that contains a list of URLs to crawl.
    // Here we download and parse the list of URLs from an external file.
    const requestList = new Apify.RequestList({
        sources: urlList,
    });
    await requestList.initialize();

    // Create an instance of the CheerioCrawler class - a crawler
    // that automatically loads the URLs and parses their HTML using the cheerio library.
    const crawler = new Apify.CheerioCrawler({
        // Let the crawler fetch URLs from our list.
        requestList,

        // The crawler downloads and processes the web pages in parallel, with a concurrency
        // automatically managed based on the available system memory and CPU (see AutoscaledPool class).
        // Here we define some hard limits for the concurrency.
        minConcurrency: 10,
        maxConcurrency: 50,

        // On error, retry each page at most once.
        maxRequestRetries: 1,

        // Increase the timeout for processing of each page.
        handlePageTimeoutSecs: 60,

        // This function will be called for each URL to crawl.
        // It accepts a single parameter, which is an object with the following fields:
        // - request: an instance of the Request class with information such as URL and HTTP method
        // - html: contains raw HTML of the page
        // - $: the cheerio object containing parsed HTML
        handlePageFunction: async ({ request, html, $ }) => {
            console.log(`Processing ${request.url}...`);

            // Extract data from the page using cheerio.
            const facts = []
            $('p', '.quickfacts').each(function() {
                facts.push($(this).text())
            })
            const factsObj = {}
	
			//clean up formatting of object
			for(var i=0; i<facts.length; i++) {
				var str = facts[i].toLowerCase().replace('/',' ').replace('-', ' ')
				var split = str.split(':')
				var keyStr = split[0].trim().replace(' ', '_')
				var key = keyStr.replace(' ', '_')
				factsObj[key] = split[1].trim()	
            }
			
			// Define plant object
            const plant = {
                url: request.url,
				latinName: $('.crossgenus', 'h5').text() + $('.genus', 'h5').text() + $('.species', 'h5').text()+ $('.subspecies', 'h5').text() + $('.designate', 'h5').text() + $('.cultivar', 'h5').text(),
				commonName: $('.common-name').text(),
                imgUrl: $('.show').attr('src'),
                plantType: factsObj.plant_type,
                foliage: factsObj.foliage_type,
                hardiness: factsObj.hardiness,
                exposure: factsObj.sun_light_exposure,
                waterReq: factsObj.water_requirements,
                seasonalInt: factsObj.seasonal_interest
            }
            
            const plantObj = JSON.stringify(plant)

			// Write plant information to file
            await fs.appendFile('plants.json', `${plantObj}` + ',', (err) => {
				if (err) throw err
			})

			// Chose not to use this method as it creates individual JSON files
            // Store the results to the default dataset. In local configuration,
            // the data will be stored as JSON files in ./apify_storage/datasets/default
            // await Apify.pushData({
            //     url: request.url,
			// 	latinName: $('.crossgenus').text() + $('.genus').text() + $('.species').text() + $('.cultivar').text(),
			// 	common_name: $('.common-name').text(),
            //     imgUrl: $('.show').attr('src'),
            //     plant_type: factsObj.plant_type,
            //     foliage: factsObj.foliage_type,
            //     hardiness: factsObj.hardiness,
            //     exposure: factsObj.sun_light_exposure,
            //     waterReq: factsObj.water_requirements,
            //     seasonalInt: factsObj.seasonal_interest
            // });
        },

        // This function is called if the page processing failed more than maxRequestRetries+1 times.
        handleFailedRequestFunction: async ({ request }) => {
            console.log(`Request ${request.url} failed twice.`);
        },
    });

    // Run the crawler and wait for it to finish.
    await crawler.run();

    console.log('Crawler finished.');
});